    public static function getEmojisGroupedByCategory(): array
    {
        return self::groupArray(self::$emojis, 'category');
    }

    /**
     * Return a single emoji
     *
     * @param string $name Name of the emoji
     * @return array Is empty if no matching emoji is found
     */
    public static function getEmoji($name = ''): array
    {
        $key = array_search($name, array_column(self::$emojis, 'name'), true);
        return ($key === false)? [] : self::$emojis[$key];
    }

